function [N,DN] = shapeFnWedge(x)

r = x(1) ; s = x(2) ; t = 1-r-s ; zhi = x(3) ;  

N = [ 0.5*(1-zhi)*r , 0.5*(1-zhi)*s , 0.5*(1-zhi)*t, ... 
	0.5*(1+zhi)*r , 0.5*(1+zhi)*s , 0.5*(1+zhi)*t] 

DN = [ 	0.5*(1-zhi), 0, -0.5*r ;  ...
	0 , 0.5*(1-zhi) , -0.5*s ;  ...
	-0.5*(1-zhi), -0.5*(1-zhi) , -0.5*t ; ... 
 	0.5*(1+zhi), 0,  0.5*r ;  ...
	0 , 0.5*(1+zhi) ,  0.5*s ;  ...
	-0.5*(1+zhi), -0.5*(1+zhi) ,  0.5*t ] ;

end
