function [N,DN] = shapeFnTetra2(r) 

L1 = 1-r(1)-r(2)-r(3);
L2 = r(1) ; 
L3 = r(2) ; 
L4 = r(3) ; 

N(1)	=	L1*(2*L1-1) ;
N(2)	=	L2*(2*L2-1) ;
N(3)	=	L3*(2*L3-1) ;
N(4)	=	L4*(2*L4-1) ;

N(5)	=	4 * L2 * L1 ; 
N(6)	=	4 * L3 * L1 ; 
N(7)	=	4 * L4 * L1 ; 
N(8)	=	4 * L2 * L3 ; 
N(9)	=	4 * L3 * L4 ; 
N(10)	=	4 * L4 * L2 ; 

DN = zeros(10,3) ; 

DN(1,1) = -(4*L1 - 1.0) ; 
DN(1,2) = -(4*L1 - 1.0) ; 
DN(1,3) = -(4*L1 - 1.0) ; 

DN(2,1) = 4*L2 - 1.0 ; 
DN(3,2) = 4*L3 - 1.0 ; 
DN(4,3) = 4*L4 - 1.0 ; 


DN(5,1) = 4*L1 - 4*L2; 
DN(5,2) = -4*L2 ; 
DN(5,3) = -4*L2 ; 

DN(6,1) = -4*L3 ; 
DN(6,2) = 4*L1 - 4*L3; 
DN(6,3) = -4*L3 ; 

DN(7,1) = -4*L4 ; 
DN(7,2) = -4*L4 ; 
DN(7,3) = 4*L1 - 4*L4; 

DN(8,1) = 4*L3 ; 
DN(8,2) = 4*L2 ; 

DN(9,2) = 4*L4 ; 
DN(9,3) = 4*L3 ; 

DN(10,1) = 4*L4 ; 
DN(10,3) = 4*L2 ; 

end
